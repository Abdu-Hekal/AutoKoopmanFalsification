function [tout, yout] = runPacemaker(T,~,u)
    
    assignin('base','u',u);
    assignin('base','T',T);
    
    result = sim('pacemaker', ...
        'StopTime', 'T', ...
        'LoadExternalInput', 'on', 'ExternalInput', 'u', ...
        'SaveTime', 'on', 'TimeSaveName', 'tout', ...
        'SaveOutput', 'on', 'OutputSaveName', 'yout', ...
        'SaveFormat', 'Array');
    tout = result.tout;
    yout = double(result.yout(:,3));
end
